# What is Module?
# Module is a file containing Python definitions and statements.
# Module can define functions, classes and variables.
# Module can also include runnable code.
# Module can be imported and used in another module.
# Module can be reloaded if changes have been made to the module.
# Module can be used to organize code logically.
# Module can be used to avoid name clashes.
# Module can be used to implement shared namespaces.
# Module can be used to implement the import system.
# Module can be used to implement the Python standard library.

import math

print(math.pi)
print(math.e)
print(math.sqrt(16))
print(math.pow(2,5))
print(math.floor(3.9))
print(math.ceil(3.9))
print(math.factorial(5))
print(math.sin(90))
print(math.cos(90))
print(math.tan(90))
print(math.log(10))
print(math.log10(10))
print(math.log2(10))
print(math.degrees(1))
print(math.radians(180))

